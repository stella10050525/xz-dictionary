// 自定义词汇存储键
const CUSTOM_WORDS_KEY = "xz_custom_words";
const VOCABULARY_KEY = "xz_vocabulary";

// 获取自定义词汇
function getCustomWords() {
    const stored = localStorage.getItem(CUSTOM_WORDS_KEY);
    return stored ? JSON.parse(stored) : {};
}

// 保存自定义词汇
function saveCustomWord(wordData) {
    const customWords = getCustomWords();
    customWords[wordData.word.toLowerCase()] = wordData;
    localStorage.setItem(CUSTOM_WORDS_KEY, JSON.stringify(customWords));
}

// 获取生词本
function getVocabulary() {
    const stored = localStorage.getItem(VOCABULARY_KEY);
    return stored ? JSON.parse(stored) : [];
}

// 保存到生词本
function saveToVocabulary(wordData) {
    let vocabulary = getVocabulary();
    const index = vocabulary.findIndex(item => item.word === wordData.word);
    
    if (index === -1) {
        vocabulary.unshift(wordData);
        localStorage.setItem(VOCABULARY_KEY, JSON.stringify(vocabulary));
        return true;
    }
    return false;
}

// 从生词本删除
function removeFromVocabulary(word) {
    let vocabulary = getVocabulary();
    vocabulary = vocabulary.filter(item => item.word !== word);
    localStorage.setItem(VOCABULARY_KEY, JSON.stringify(vocabulary));
}

// 检查是否在生词本中
function isInVocabulary(word) {
    const vocabulary = getVocabulary();
    return vocabulary.some(item => item.word === word);
}

// 清空生词本
function clearVocabulary() {
    if (confirm('确定要清空整个生词本吗？')) {
        localStorage.removeItem(VOCABULARY_KEY);
        return true;
    }
    return false;
}
